################################
# Os operadores em Python são:
# <  # menor que
# <= # menor ou igual que
# >  # maior que
# >= # maior ou igual que
# == # igual
# != # diferente
################################
print('='*40)
print('Exemplos de aplicações dos operadores de comparação...')
print(f'2 < 10 => \033[32m{2 < 10}\033[0;0m')
print(f'17 <= 7 => \033[31m{17 <= 7}\033[0;0m')
print(f'2 > 11 => \033[31m{2 > 11}\033[0;0m')
print(f'10 >= 10 => \033[32m{10 >= 10}\033[0;0m')
print(f'42 == 24 => \033[31m{42 == 24}\033[0;0m')
print(f'666 != 137 => \033[32m{666 != 137}\033[0;0m')
print(f'8**2 == 60 + 4 => \033[32m{8**2 == 60 + 4}\033[0;0m')
print(f'100 != 99 + 3 => \033[32m{100 != 99 + 3}\033[0;0m')
